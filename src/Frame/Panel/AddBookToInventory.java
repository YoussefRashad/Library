    private void Reset() {
        jLabelSuccessOrFail.setText("Add Book To Inventory ...!");
        jTextField1.setText("");
        jComboBoxNameInventory.setSelectedIndex(0);

        jTextFieldIDBook.setText("");
        jTextFieldNameBook.setText("");
        jTextFieldNoOfBook.setText("");
        jTextFieldSalary.setText("");
        jComboBoxNameInventory.setSelectedIndex(0);
        
        jPanelID2.setVisible(false);
        jPanelID.setVisible(false);
        jPanelBOOKNO.setVisible(false);
        jPanelName.setVisible(false);
        jPanelSalary.setVisible(false);
    }

    private void AddbookToIventory() {
        jPanelID2.setVisible(false);
        jPanelID.setVisible(false);
        jPanelBOOKNO.setVisible(false);
        jPanelName.setVisible(false);
        jPanelSalary.setVisible(false);
        if (!jTextFieldIDBook.getText().equals("") && !jTextFieldNameBook.getText().equals("") && !jTextFieldNoOfBook.getText().equals("") && !jTextFieldSalary.getText().equals("")) {
            Validation validate = new Validation();
            if (validate.checkID(jTextFieldIDBook.getText())) {
                if (validate.checkName(jTextFieldNameBook.getText())) {
                    if (validate.checkNumber(jTextFieldNoOfBook.getText())) {
                        if (validate.checkSalary(jTextFieldSalary.getText())) {
                            Inventory in = new Inventory();
                            if (!in.DublicateBooksInInventory(Integer.parseInt(jTextFieldIDBook.getText()))) {
                                in = in.getInventory(jComboBoxNameInventory.getSelectedItem().toString());
                                if (in != null) {
                                    
                                    in.setBook(Integer.parseInt(jTextFieldIDBook.getText()), Double.parseDouble(jTextFieldSalary.getText()),
                                            Integer.parseInt(jTextFieldNoOfBook.getText()), jTextFieldNameBook.getText());
                                    
                                    if (in.Edit(in.getIDInventory())) {
                                        jLabelSuccessOrFail.setText("Adding Is Successful!");
                                        Reset();
                                    } else {
                                        jLabelSuccessOrFail.setText("Faild To Add The Data. Please Try Again.");
                                    }
                                } else {
                                    jLabelSuccessOrFail.setText("Not Found ...! :( ");
                                }
                            } else {
                                jLabelSuccessOrFail.setText("Not Allowed New Book ID ");
                                jPanelID2.setVisible(true);
                                JOptionPane.showMessageDialog(null, "Not Allowed  Book ID, Admin  ID is used. Please Try Again.");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Invalid Salary. Please Try Again.");
                            jPanelSalary.setVisible(true);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Invalid No Of Book. Please Try Again.");
                        jPanelBOOKNO.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid Book Name. Please Try Again.");
                    jPanelName.setVisible(true);

                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Book ID. Please Try Again.");
                jPanelID.setVisible(true);

            }
        } else {
            JOptionPane.showMessageDialog(null, "Please Fill The Empty Fields.");
        }
    }

