    private void resetTable() {

        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            while (jTable1.getRowCount() > 0) {
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    model.removeRow(i);
                }
            }
            model.removeRow(0);
        } catch (Exception e) {
            ;
        }

    }

    private void search() {

        AddRowsToTable();
        jTextField1.setText(String.valueOf(new Book().cat.searchForGetCategoryID(jComboBox1.getSelectedItem().toString())));
        jTextField2.setText(String.valueOf(new Book().cat.searchForCategoryNameSubSectionPanel(jComboBox1.getSelectedItem().toString())));

    }

    public void AddRowsToTable() {
        resetTable();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] rowData = new Object[6];
        ArrayList<Book> book = new Book().listSpecificCategory(jComboBox1.getSelectedItem().toString());
        for (Book a : book) {
            rowData[0] = a.getIDBook();
            rowData[1] = a.getNameBook();
            String DOB[] = a.Author.getDOPublisher().split("#");
            rowData[2] = DOB[2] + "/" + DOB[1] + "/" + DOB[0];
            rowData[3] = a.getNoOfBooks();
            rowData[4] = a.Reciever.getName();
            rowData[5] = a.getSalary();
            model.addRow(rowData);
        }
    }

    private void reset() {
        jTextField1.setText("");
        jTextField2.setText("");
        resetTable();
        if (jComboBox1.getItemCount() > 0) {
            jComboBox1.setSelectedIndex(0);
        }
    }

