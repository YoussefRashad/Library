    private void Search() {

        visable();
        Frist_Half();
        if (!jTextFieldSearchKey.getText().equals("")) {
            Validation c = new Validation();
            if (c.checkID(jTextFieldSearchKey.getText())) {
                Section s = new Section();
                s = s.getSection(Integer.parseInt(jTextFieldSearchKey.getText()));
                if (s != null) {
                    second_Half();
                    jLabelSuccessOrFail.setText("Founded ...! :) ");
                    jTextFieldSectionID.setText("" + s.getSectionID());
                } else {
                    jLabelSuccessOrFail.setText("Not Found ...! :( ");
                    jPanelWrongSearchKey.setVisible(true);
                    Frist_Half();
                }

            } else {
                jLabelSuccessOrFail.setText("Not valid ID Section :( ");
                JOptionPane.showMessageDialog(null, "Not valid ID Section Try Again :(\nHint: ID Section Length should more than 4 .. ;)");
                jPanelWrongID.setVisible(true);
                Frist_Half();
            }
        } else {
            jLabelSuccessOrFail.setText("Missing Required ID ... !  Please, complete it before submit ...! :(");
            jPanelWrongSearchKey.setVisible(true);
            Frist_Half();
        }
    }

    private void remove() {

        visable();
        Frist_Half();
        if (!jTextFieldSearchKey.getText().equals("") && !jTextFieldSectionID.getText().equals("")) {
            Validation c = new Validation();
            if (c.checkID(jTextFieldSearchKey.getText())) {
                Section s = new Section();
                second_Half();
                if (s.Remove(Integer.parseInt(jTextFieldSearchKey.getText()))) {

                    jLabelSuccessOrFail.setText("deleted successfully");
                    reset();
                } else {
                    jLabelSuccessOrFail.setText("not deleted");
                    jPanelWrongSearchKey.setVisible(true);
                }

            } else {
                JOptionPane.showMessageDialog(null, "wrong ID");
                jPanelWrongID.setVisible(true);
                Frist_Half();

            }
        } else {
            jLabelSuccessOrFail.setText("missing required field...please enter ID");
            Frist_Half();
        }
    }

    private void Update() {

        visable();
        Frist_Half();
        if (!jTextFieldSearchKey.getText().equals("") && !jTextFieldSectionID.getText().equals("")) {
            Validation c = new Validation();
            if (c.checkID(jTextFieldSectionID.getText())) {
                Section s = new Section();
                second_Half();
                s = s.getSection(Integer.parseInt(jTextFieldSearchKey.getText()));
                if(!jTextFieldSectionID.getText().equals(jTextFieldSearchKey.getText())){
                if (!s.Dublicate(Integer.parseInt(jTextFieldSectionID.getText()))) {
                    s.setSectionID(Integer.parseInt(jTextFieldSectionID.getText()));
                    if (s.Edit(Integer.parseInt(jTextFieldSearchKey.getText()))) {
                        jlabelSection.setText("updated successfully");
                        reset();
                    } else {
                        jLabelSuccessOrFail.setText("not updated");
                        jPanelWrongSearchKey.setVisible(true);
                       second_Half();

                    }
                } else {
                    jLabelSuccessOrFail.setText("Not Allowed New ID Section :(");
                    jPanelWrongNotAllowed.setVisible(true);
                    JOptionPane.showMessageDialog(null, "Not Allowed ID Section , ID Section is Reserved...!\n Try Again :(");
              second_Half();
                }
            }
                else{
                    s.setSectionID(Integer.parseInt(jTextFieldSectionID.getText()));
                    if (s.Edit(Integer.parseInt(jTextFieldSearchKey.getText()))) {
                        jlabelSection.setText("updated successfully");
                        reset();
                    } else {
                        jLabelSuccessOrFail.setText("not updated");
                        jPanelWrongSearchKey.setVisible(true);
                       second_Half();

                    }
                }

            } else {
                JOptionPane.showMessageDialog(null, "wrong ID");
                jPanelWrongID.setVisible(true);
                second_Half();
            }
        } else {
            jLabelSuccessOrFail.setText("missing required field...please enter ID");
           second_Half();
        }
    }

