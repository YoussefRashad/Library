    private void AddBooks() {
        visible();
         if (!jTextFieldIDBook.getText().equals("") && !jTextFieldNameBook.getText().equals("")
                && !jTextFieldNoOfBook.getText().equals("") && !jTextFieldPublisherAuthor.getText().equals("")
                /*&&!jTextFieldRentPerDay.getText().equals("")*/ && !jTextFieldSalary.getText().equals("")) {
            Validation v = new Validation();
            if (v.checkID(jTextFieldIDBook.getText())) {
                if (v.checkName(jTextFieldNameBook.getText())) {
                    if (v.checkName(jTextFieldPublisherAuthor.getText())) {
                        if (v.checkNumber(jTextFieldNoOfBook.getText())) {
                            if (v.checkSalaryOfBook(jTextFieldSalary.getText())) {
                                //if(v.checkSalaryOfBook(jTextFieldRentPerDay.getText())){

                                Book b = new Book();
                                if (!b.Dublicate(Integer.parseInt(jTextFieldIDBook.getText()))) {
                                    b.setIDBook(Integer.parseInt(jTextFieldIDBook.getText()));
                                    b.setNameBook(jTextFieldNameBook.getText());
                                    b.setNoOfBooks(Integer.parseInt(jTextFieldNoOfBook.getText()));
                                    //b.setRentPerDay(Double.parseDouble(jTextFieldRentPerDay.getText()));
                                    b.setSalary(Double.parseDouble(jTextFieldSalary.getText()));
                                    b.Author.setNamePublisher(jTextFieldPublisherAuthor.getText());
                                    b.Author.setDOPublisher(jComboBoxyear.getSelectedItem().toString() + "#" + jComboBoxmonth.getSelectedItem().toString() + "#"
                                            + jComboBoxday.getSelectedItem().toString() + "#");
                                    b.Reciever.setName(jComboBoxReceiver.getSelectedItem().toString());
                                    b.comment.setComment(jTextAreaComment.getText());
                                    b.cat.setNameCategory(jComboBoxCategory.getSelectedItem().toString());
                                    b.cat.setIDCategory(b.cat.searchForGetCategoryID(b.cat.getNameCategory()));
                                    
                                    b.cat.ss.setSubSectionName(b.cat.searchForCategoryNameSubSectionPanel(b.cat.getNameCategory()));
                                    b.cat.ss.setSubSectionID(b.cat.ss.SearchForSubSectionIDPanel(b.cat.ss.getSubSectionName()));
                                    b.cat.ss.sec.setSectionID(b.cat.ss.searchForSubInSectionPanel(b.cat.ss.getSubSectionName()));
                                    
                                    if (b.Add()) {
                                        jLabelSuccessOrFail.setText("Added Successfully ...! :) ");
                                        resetPanel();
                                    } else {
                                        jLabelSuccessOrFail.setText("Failed To Insert ... ! :( ");
                                    }
                                } else {
                                    jLabelSuccessOrFail.setText("Not Allowed New Book ID ");
                                    jPanelWrongID.setVisible(true);
                                    JOptionPane.showMessageDialog(null, "Not Allowed Book ID,Book ID is used. Please Try Again.");
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Not valid Salary Try Again :(\n");
                                jPanelWrongSalary.setVisible(true);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Not valid NO OF BOOK Try Again :(\n");
                            jPanelWrongBookNo.setVisible(true);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Not valid Name Of Author Try Again :(");
                        jPanelWrongAuthor.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Not valid Name Of book Try Again :(");
                    jPanelWrongName.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Not valid ID Of Book Try Again :(");
                jPanelWrongID.setVisible(true);
            }

        } else {
            jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
        }
    }

    private void visible() {
        jPanelWrongID.setVisible(false);
        jPanelWrongName.setVisible(false);
        jPanelWrongAuthor.setVisible(false);
        jPanelWrongBookNo.setVisible(false);

        jPanelWrongSalary.setVisible(false);
    }
