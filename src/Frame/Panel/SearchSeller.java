    private void Update() {
        visable();
        Frist_Half();
        if (!jTextFieldSearchKey.getText().equals("") && !jTextFieldID.getText().equals("") && !jTextFieldName.getText().equals("") && !jTextFieldUserName.getText().equals("") && !jTextFieldEmail.getText().equals("") && !jTextFieldPass.getText().equals("") && !jTextFieldIDNational.getText().equals("") && !jTextFieldPhoneNo.getText().equals("") && !jTextFieldOfficeHour.getText().equals("") && !jTextFieldqualification.getText().equals("") && !jTextFieldSalary.getText().equals("")) {
            Validation v = new Validation();
            if (v.checkID(jTextFieldID.getText())) {
                if (v.checkName(jTextFieldName.getText())) {
                    if (v.checkUserName(jTextFieldUserName.getText())) {
                        if (v.checkEmail(jTextFieldEmail.getText())) {
                            if (v.checkPass(jTextFieldPass.getText())) {
                                if (v.checkIDNational(jTextFieldIDNational.getText())) {
                                    if (v.checkPhoneNo(jTextFieldPhoneNo.getText())) {
                                        if (v.checkSalary(jTextFieldSalary.getText())) {

                                            Seller s = new Seller();
                                            s = (Seller) s.get(Integer.parseInt(jTextFieldSearchKey.getText()));
                                            if (s != null) {
                                                if(!jTextFieldID.getText().equals(jTextFieldSearchKey.getText())){
                                                if (!s.Dublicate(Integer.parseInt(jTextFieldID.getText()))) {
                                                    second_Half();
                                                    s.setEmail(jTextFieldEmail.getText());
                                                    if (jComboBox2.getSelectedItem().equals("Male")) {
                                                        s.setGender('M');
                                                    } else {
                                                        s.setGender('F');
                                                    }
                                                    s.setID(Integer.parseInt(jTextFieldID.getText()));
                                                    s.setIDNational((jTextFieldIDNational.getText()));
                                                    s.setName(jTextFieldName.getText());
                                                    s.setPhoneNo(jTextFieldPhoneNo.getText());
                                                    s.setUserName(jTextFieldUserName.getText());
                                                    s.setPass(jTextFieldPass.getText());
                                                    s.setOfficeHour(Integer.parseInt(jTextFieldOfficeHour.getText()));
                                                    s.setQualification(jTextFieldqualification.getText());
                                                    s.setSalary(Double.parseDouble(jTextFieldSalary.getText()));

                                                    if (s.Edit(Integer.parseInt(jTextFieldSearchKey.getText()))) {
                                                        jlabelsearchSeller.setText("updated Successfully ....!");
                                                        reset();
                                                    } else {
                                                        jlabelsearchSeller.setText("Failed To update...!");
                                                    }
                                                } else {
                                                    jlabelsearchSeller.setText("Not Allowed New Seller ID ");
                                                    jPanelWrongID.setVisible(true);
                                                    JOptionPane.showMessageDialog(null, "Not Allowed Seller ID,Seller ID is used. Please Try Again.");
                                                    second_Half();
                                                }
                                                
                                                }else{
                                                    second_Half();
                                                    s.setEmail(jTextFieldEmail.getText());
                                                    if (jComboBox2.getSelectedItem().equals("Male")) {
                                                        s.setGender('M');
                                                    } else {
                                                        s.setGender('F');
                                                    }
                                                    s.setID(Integer.parseInt(jTextFieldID.getText()));
                                                    s.setIDNational((jTextFieldIDNational.getText()));
                                                    s.setName(jTextFieldName.getText());
                                                    s.setPhoneNo(jTextFieldPhoneNo.getText());
                                                    s.setUserName(jTextFieldUserName.getText());
                                                    s.setPass(jTextFieldPass.getText());
                                                    s.setOfficeHour(Integer.parseInt(jTextFieldOfficeHour.getText()));
                                                    s.setQualification(jTextFieldqualification.getText());
                                                    s.setSalary(Double.parseDouble(jTextFieldSalary.getText()));
                                                    if (s.Edit(Integer.parseInt(jTextFieldSearchKey.getText()))) {
                                                        jlabelsearchSeller.setText("updated Successfully ....!");
                                                        reset();
                                                    } else {
                                                        jlabelsearchSeller.setText("Failed To update...!");
                                                    }
                                                }
                                            } else {
                                                jlabelsearchSeller.setText("Not Found Seller ...! Please, Enter Correct ID ...! :( ");
                                                jPanel1.setVisible(true);
                                            }

                                        } else {
                                            JOptionPane.showMessageDialog(null, "Not valid Salary Try Again :(\nHint: Salary should more than 1000 .. ;)");
                                            jPanelWrongsalary.setVisible(true);
                                            second_Half();
                                        }
                                    } else {
                                        JOptionPane.showMessageDialog(null, "Not valid Phone No Try Again :(\nHint: Phone No should  equal 11 numbers if it mobile\n or more than 8 with Code of Governorate if it Tel  .. ;)");
                                        jPanelWrongPhoneNo.setVisible(true);
                                        second_Half();
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "Not valid ID National Try Again :(\nHint: ID National Length should equal to 14 Number .. ;)");
                                    jPanelWrongIDNational.setVisible(true);
                                    second_Half();
                                }

                            } else {
                                JOptionPane.showMessageDialog(null, "Not valid Password length ,Should it Length\nis more than 6 OR Password has (*,/,=,+,...)\n ");
                                jPanelWrongPassword.setVisible(true);
                                second_Half();
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "\"Not valid Input for Email Try Again :(\"");
                            jPanelWrongEmail.setVisible(true);
                            second_Half();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "\"Not valid User Name Try Again :(\"");
                        jPanelWrongUserName.setVisible(true);
                        second_Half();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                    jPanelWrongName.setVisible(true);
                    second_Half();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Not valid ID Try Again :(\nHint: ID Length should more than 4 .. ;)");
                jPanelWrongID.setVisible(true);
                second_Half();
            }
        } else {
            jlabelsearchSeller.setText("Missing Required Fields ... !  Please, complete them before submit ...! :)");
            second_Half();
        }
    }

    private void Remove() {
        visable();
        Frist_Half();
        if (!jTextFieldSearchKey.getText().equals("") && !jTextFieldID.getText().equals("")) {
            Validation c = new Validation();
            if (c.checkID(jTextFieldSearchKey.getText())) {
                Seller s = new Seller();
                second_Half();
                if (s.Remove(Integer.parseInt(jTextFieldSearchKey.getText()))) {

                    jlabelsearchSeller.setText("Delted successfully");
                    reset();

                } else {
                    jlabelsearchSeller.setText("not deleted");
                    Frist_Half();
                }

            } else {
                JOptionPane.showMessageDialog(null, "wrong ID");
                jPanel1.setVisible(true);
                Frist_Half();
            }
        } else {
            jlabelsearchSeller.setText("missing required field...please enter ID");
            Frist_Half();
        }
    }

    private void search() {
        visable();
        Frist_Half();
        if (!jTextFieldSearchKey.getText().equals("")) {
            Validation c = new Validation();
            if (c.checkID(jTextFieldSearchKey.getText())) {
                Seller s = new Seller();
                s = (Seller) s.get(Integer.parseInt(jTextFieldSearchKey.getText()));
                if (s != null) {
                    second_Half();
                    jTextFieldID.setText("" + s.getID());
                    jTextFieldName.setText(s.getName());
                    jTextFieldUserName.setText(s.getUserName());
                    jTextFieldEmail.setText(s.getEmail());
                    jTextFieldPass.setText(s.getPass());
                    if (s.getGender() == 'M') {
                        jComboBox2.setSelectedItem("Male");
                    }
                    if (s.getGender() == 'F') {
                        jComboBox2.setSelectedItem("Female");
                    }
                    jTextFieldIDNational.setText("" + s.getIDNational());
                    jTextFieldPhoneNo.setText(s.getPhoneNo());
                    jTextFieldOfficeHour.setText(s.getOfficeHour() + "");
                    jTextFieldqualification.setText(s.getQualification());
                    jTextFieldSalary.setText(s.getSalary() + "");
                    
                    jlabelsearchSeller.setText("found");
                } else {
                    jlabelsearchSeller.setText("not found");
                    Frist_Half();
                }
            } else {
                JOptionPane.showMessageDialog(null, "wrong ID");
                jPanel1.setVisible(true);
                Frist_Half();
            }
        } else {
            jlabelsearchSeller.setText("missing required field...please enter ID");
            Frist_Half();
        }
    }

