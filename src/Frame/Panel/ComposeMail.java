    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        // TODO add your handling code here:
        jPanelWrongTo.setVisible(false);
        jPanelWrongCC.setVisible(false);
        jPanelWrongSubject.setVisible(false);
        if(!jTextArea.getText().equals("") && !jTextFieldto.getText().equals("") && !jTextFieldsubject.getText().equals(""))
        {
            Validation v = new Validation();
            if(v.checkEmail(jTextFieldto.getText())){
                if(v.checkName(jTextFieldcc.getText())){
                    if(v.checkName(jTextFieldsubject.getText())){
                        Mail m = new Mail();
                        m.setToEmail(jTextFieldto.getText());
                        m.setCcEmail(jTextFieldcc.getText());
                        m.setEmailbody(jTextArea.getText());
                        m.setSubject(jTextFieldsubject.getText());
                        try {
                            m.generateAndSentMessage();
                            jLabelSuccessOrFalse.setText("Your message has been sent ...! :)");
                            resetPanel();
                        } catch (MessagingException ex) {
                            jLabelSuccessOrFalse.setText("Your message hasn't been delivered ... ! :(");
                            Logger.getLogger(ComposeMail.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(jLabel4, "Not valid Input Subject Try Again :(");
                        jPanelWrongSubject.setVisible(true);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(jLabel4, "Not valid Input CC Try Again :(");
                    jPanelWrongCC.setVisible(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(jLabel4, "Not valid Input To Try Again :(");
                jPanelWrongTo.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFalse.setText("Missing required Fields ... !  Please, complete them before submit ...! :(");
    }//GEN-LAST:event_jLabel4MouseClicked

