    private void Search() {
        if (!jTextFieldSearchKey.getText().equals("")) {
            if (new Validation().checkID(jTextFieldSearchKey.getText())) {
                Inventory b = new Inventory();
                b = b.getInventory(Integer.parseInt(jTextFieldSearchKey.getText()));
                if (b != null) {
                    second_Half();
                    jLabelSuccessOrFail.setText("Founded ...! :) ");
                    setPanel(b);
                } else {
                    jLabelSuccessOrFail.setText("Not Found ...! :( ");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Not valid ID of Inventory Try Again :(");
                jPanelWrongSearchedID.setVisible(true);
            }
        } else {
            jLabelSuccessOrFail.setText("Missing Required Inventory ID ... !  Please, complete it before submit ...! :(");
        }
    }

    private void Remove() {
        if (!jTextFieldSearchKey.getText().equals("")) {
            if (new Validation().checkID(jTextFieldSearchKey.getText())) {
                Inventory b = new Inventory();
                b = b.getInventory(jTextFieldSearchKey.getText());
                if (b != null) {
                    second_Half();
                    b.Remove(b.getIDInventory());
                    jLabelSuccessOrFail.setText("Removed Successfully ...! :)");
                    resetPanel();
                } else {
                    jLabelSuccessOrFail.setText("Failed Removed ...! :(");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Not valid ID Of Inventory Try Again :(");
                jPanelWrongSearchedID.setVisible(true);
            }
        }
    }

    private void Updata() {

        if (!jTextFieldID.getText().equals("") && !jTextFieldName.getText().equals("") && !jTextFieldSearchKey.getText().equals("")) {
            Validation v = new Validation();
            if (v.checkID(jTextFieldSearchKey.getText())) {
                if (v.checkID(jTextFieldID.getText())) {
                    if (v.checkName(jTextFieldName.getText())) {
                        Inventory b = new Inventory();
                        b = b.getInventory(jTextFieldSearchKey.getText());
                        if (b != null) {
                            second_Half();
                            int previousID = b.getIDInventory();
                            b.setIDInventory(Integer.parseInt(jTextFieldID.getText()));
                            b.setNameInventory(jTextFieldName.getText());

                            if (b.Edit(previousID)) {
                                resetPanel();
                                jLabelSuccessOrFail.setText("Successfully Updated ...! :)");
                            } else {
                                jLabelSuccessOrFail.setText("error in Updated ...! :(");
                            }
                        } else {
                            jLabelSuccessOrFail.setText("Not Found Inventory ...! Please, Enter Correct Inventory ID ...! :( ");
                            jPanelWrongSearchedID.setVisible(true);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Not valid Name Of Inventory Try Again :(");
                        jPanelWrongName.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Not valid ID Of Inventory Try Again :(");

                    jPanelWrongID2.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Not valid ID Of Inventory Try Again :(");
                jPanelWrongSearchedID.setVisible(true);
            }
        } else {
            jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
        }
    }

