    private void AddAdmins() {
        visable();
         if (!jTextFieldID.getText().equals("") && !jTextFieldAdress.getText().equals("") && !jTextFieldAge.getText().equals("") && !jTextFieldEmail.getText().equals("")
                && !jTextFieldIDNational.getText().equals("") && !jTextFieldName.getText().equals("") && !jTextFieldPhoneNo.getText().equals("") && !jTextFieldUserName.getText().equals("")
                && !jPasswordFieldPass.getText().equals("") && !jTextFieldPath.getText().equals("")) {
            // AllCheck
            Validation v = new Validation();
            if (v.checkID(jTextFieldID.getText())) {
                if (v.checkName(jTextFieldName.getText())) {
                    if (v.checkUserName(jTextFieldUserName.getText())) {
                        if (v.checkEmail(jTextFieldEmail.getText())) {
                            if (v.checkPass(jPasswordFieldPass.getText())) {
                                if (v.checkPass(jPasswordFieldRePassword.getText())) {
                                    if (v.checkIDNational(jTextFieldIDNational.getText())) {
                                        if (v.checkPhoneNo(jTextFieldPhoneNo.getText())) {
                                            if (v.checkAge(jTextFieldAge.getText())) {
                                                if (v.checkAddress(jTextFieldAdress.getText())) {
                                                    if (v.checkImage(jTextFieldPath.getText())) {
                                                        //EndCheck
                                                        //StartAdd
                                                        Admin a = new Admin();
                                                        if (!a.Dublicate(Integer.parseInt(jTextFieldID.getText()))) {
                                                            if (jPasswordFieldPass.getText().equals(jPasswordFieldRePassword.getText())) {
                                                                a.setAddress(jTextFieldAdress.getText());
                                                                a.setAge(Integer.parseInt(jTextFieldAge.getText()));
                                                                a.setDOB(jComboBoxyearBirth.getSelectedItem().toString() + "#" + jComboBoxmonthBirth.getSelectedItem().toString() + "#"
                                                                        + jComboBoxdayBirth.getSelectedItem().toString() + "#");
                                                                a.setEmail(jTextFieldEmail.getText());
                                                                if (jComboBoxGender.getSelectedItem().equals("Male")) {
                                                                    a.setGender('M');
                                                                } else {
                                                                    a.setGender('F');
                                                                }
                                                                a.setID(Integer.parseInt(jTextFieldID.getText()));
                                                                a.setIDNational((jTextFieldIDNational.getText()));
                                                                a.setName(jTextFieldName.getText());
                                                                a.setPhoneNo(jTextFieldPhoneNo.getText());
                                                                a.setUserName(jTextFieldUserName.getText());
                                                                a.setProfileImage(jTextFieldPath.getText());
                                                                a.setPass(jPasswordFieldPass.getText());
                                                                if (a.Add()) {
                                                                    jLabelSuccessOrFail.setText("Added Successfully ...! :) ");
                                                                    resetPanel();
                                                                } else {
                                                                    jLabelSuccessOrFail.setText("Failed To Insert ... ! :( ");
                                                                }
                                                            } else {
                                                                JOptionPane.showMessageDialog(null, "Not valid password & Repassword not similar please Try Again :( ");
                                                                jLabelSuccessOrFail.setText("Pass & Re Pass Not Similar ...! :( ");
                                                                jPanelWrongRePassword.setVisible(true);jPanelWrongPassword.setVisible(true);
                                                            }
                                                            

                                                        } else {
                                                            jLabelSuccessOrFail.setText("Not Allowed New Admin ID ");
                                                            jPanelWrongID.setVisible(true);
                                                            JOptionPane.showMessageDialog(null, "Not Allowed  Admin ID, Admin  ID is used. Please Try Again.");
                                                        }

                                                    } else {
                                                        JOptionPane.showMessageDialog(null, "Not valid File Path Try Again :(\nHint: File PAth should Contain .png/.jpg .. ;)");
                                                        jPanelWrongProfileImage.setVisible(true);
                                                    }
                                                } else {
                                                    JOptionPane.showMessageDialog(null, "Not valid Input for Address Try Again :(");
                                                    jPanelWrongAddress.setVisible(true);
                                                }
                                            } else {
                                                JOptionPane.showMessageDialog(null, "Not valid Age Try Again :(\nHint: Age should more than 18 .. ;)");
                                                jPanelWrongAge.setVisible(true);
                                            }
                                        } else {
                                            JOptionPane.showMessageDialog(null, "Not valid Phone No Try Again :(\nHint: Phone No should  equal 11 numbers if it mobile\n or more than 8 with Code of Governorate if it Tel  .. ;)");
                                            jPanelWrongPhoneNo.setVisible(true);
                                        }
                                    } else {
                                        JOptionPane.showMessageDialog(null, "Not valid ID National Try Again :(\nHint: ID National Length should equal to 14 Number .. ;)");
                                        jPanelWrongIDNational.setVisible(true);
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "Not valid RePassword length ,Should it Length\nis more than 6 OR Password has (*,/,=,+,...)\n                    Try Again :(");
                                    jPanelWrongRePassword.setVisible(true);
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Not valid Password length ,Should it Length\nis more than 6 OR Password has (*,/,=,+,...)\n                    Try Again :(");
                                jPanelWrongPassword.setVisible(true);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Not valid Input for Email Try Again :(");
                           jPanelWrongEmail.setVisible(true);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Not valid User Name Try Again :(");
                        jPanelWrongUserName.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                    jPanelWrongName.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Not valid ID Try Again :(\nHint: ID Length should more than 4 .. ;)");
               jPanelWrongID.setVisible(true);
            }

        } else {
            jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
        }

    }

