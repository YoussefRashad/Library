    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        // TODO add your handling code here:
        if (!jTextFieldID.getText().equals("") && !jTextFieldName.getText().equals("")) {

            //if (jComboBoxCurrently.getItemCount() != 0) {
            Validation c = new Validation();
            if (c.checkID(jTextFieldID.getText())) {
                if (c.checkUserName(jTextFieldName.getText())) {
                    Inventory in = new Inventory();
                    if (!in.Dublicate(Integer.parseInt(jTextFieldID.getText()))) {
                        in.setIDInventory(Integer.parseInt(jTextFieldID.getText()));
                        in.setNameInventory(jTextFieldName.getText());
                        if (in.Add()) {
                            jLabelInventory.setText("Added Successfully...!");
                            jComboBoxCurrently.addItem(jTextFieldName.getText());
                            this.reset();
                        } else {
                            jLabelInventory.setText("not Added...!");
                        }
                    } else {
                        jLabelInventory.setText("Not Allowed New Inventory ID :( ");
                        jPanelWrongID.setVisible(true);
                        JOptionPane.showMessageDialog(null, "Not Allowed  Inventory ID, Inventory  ID is used :( \nPlease Try Again.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "wrong Name");
                }

            } else {
                JOptionPane.showMessageDialog(null, "wrong ID");
                jPanelWrongID.setVisible(true);
            }

            /*} else {
                jLabelInventory.setText("No Inventories found");
                jPanelWrongName.setVisible(true);
            }*/
        } else {

            jLabelInventory.setText("missing required field...please complete all fields");
        }
        
    }//GEN-LAST:event_jLabel1MouseClicked

