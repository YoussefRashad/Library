    private void resetTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            while (jTable1.getRowCount() > 0) {
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    model.removeRow(i);
                }
            }
            model.removeRow(0);
        } catch (Exception e) {
            ;
        }

    }

    public void AddRowsToTable() {
        resetTable();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] rowData = new Object[2];
        ArrayList<Category> category = new Category().ListSpecificSubSection(jComboBox1.getSelectedItem().toString());
        for (Category a : category) {
            rowData[0] = a.getIDCategory();
            rowData[1] = a.getNameCategory();

            model.addRow(rowData);
        }
    }

    private void reset() {
        full();
        jTextField1.setText("");
        jTextField2.setText("");
        if(jComboBox1.getItemCount()>0)
            jComboBox1.setSelectedIndex(0);
        resetTable();
        Frist_Half();

    }

    private void Frist_Half() {

        jTextField1.setEditable(false);
        jTextField2.setEditable(false);

    }

