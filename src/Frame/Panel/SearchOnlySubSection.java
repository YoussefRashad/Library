    private void full() {

        jTextField1.setEditable(true);
        jTextField2.setEditable(true);

    }

    private void Search() {
        second_Half();
        jTextField1.setText(String.valueOf(new SubSection().SearchForSubSectionIDPanel(jComboBox1.getSelectedItem().toString())));
        jTextField2.setText("" + new SubSection().searchForSubInSectionPanel(jComboBox1.getSelectedItem().toString()));
        AddRowsToTable();
        Frist_Half();
    }

