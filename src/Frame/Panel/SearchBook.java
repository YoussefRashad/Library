    private void rest1() {
        try {
            while (jComboBoxSubSection.getItemCount() > 0) {
                for (int i = 0; i < jComboBoxSubSection.getItemCount(); i++) {
                    jComboBoxSubSection.removeItemAt(i);
                }
            }
            jComboBoxSubSection.removeItemAt(0);
        } catch (Exception e) {
            ;
        }

    }

    private void rest2() {
        try {
            while (jComboBoxCategory.getItemCount() > 0) {
                for (int i = 0; i < jComboBoxCategory.getItemCount(); i++) {
                    jComboBoxCategory.removeItemAt(i);
                }
            }
            jComboBoxCategory.removeItemAt(0);
        } catch (Exception e) {
            ;
        }

    }

