    public void setPanel(Category c) {
        String[] SubSectionCombo = new SubSection().ListAllNameOfSubSections();
        for (int i = 0; i < SubSectionCombo.length; i++) {
            jComboBoxSubSection.addItem(SubSectionCombo[i]);
        }
        jTextFieldName.setText(c.getNameCategory());
        jTextFieldID.setText("" + c.getIDCategory());
        jComboBoxSubSection.setSelectedItem(c.ss.getSubSectionName());
        jTextFieldSection.setText("" + c.ss.sec.getSectionID());
        jTextFieldSection.setText("" + new SubSection().searchForSubInSectionPanel(jComboBoxSubSection.getSelectedItem().toString()));
    }

