    private void Update() {
        Frist_Half();
        visiable();
        if (!jTextFieldName.getText().equals("") && !jTextFieldID.getText().equals("") && !jTextFieldSearchKey.getText().equals("")) {
            Validation v = new Validation();
            if (v.checkName(jTextFieldSearchKey.getText())) {
                if (v.checkID(jTextFieldID.getText())) {
                    if (v.checkName(jTextFieldName.getText())) {
                        Category c = new Category();
                        if(!jTextFieldID.getText().equals(jTextFieldSearchKey.getText())){
                        if (!c.Dublicate(Integer.parseInt(jTextFieldID.getText()))) {
                            c = c.getCategory(jTextFieldSearchKey.getText());
                            if (c != null) {
                                second_Half();
                                c.setNameCategory(jTextFieldName.getText());
                                c.setIDCategory(Integer.parseInt(jTextFieldID.getText()));
                                c.ss.setSubSectionName(jComboBoxSubSection.getSelectedItem().toString());
                                c.ss.setSubSectionID(new SubSection().getIDFromNameSubsection(jComboBoxSubSection.getSelectedItem().toString()));
                                SubSection ss = c.ss.getSubSection(c.ss.getSubSectionID());
                                c.ss.sec.setSectionID(ss.sec.getSectionID());
                                if (c.Edit(new Category().searchForGetCategoryID(jTextFieldSearchKey.getText()))) {
                                    resetPanel();
                                    jLabelSuccessOrFail.setText("Successfully Updated ...! :)");
                                } else {
                                    jLabelSuccessOrFail.setText("error in Updated ...! :(");
                                }
                            } else {
                                jLabelSuccessOrFail.setText("Not Found ...! :( ");
                                 jPanel3.setVisible(true);
                                 Frist_Half();
                                
                            }
                        } else {
                            jLabelSuccessOrFail.setText("Not Allowed New Category ID ");
                            jPanel8.setVisible(true);
                            JOptionPane.showMessageDialog(null, "Not Allowed Category  ID,Category  ID is used. Please Try Again.");
                            second_Half();
                        }
                    }else{
                        c = c.getCategory(jTextFieldSearchKey.getText());
                            if (c != null) {
                                second_Half();
                                c.setNameCategory(jTextFieldName.getText());
                                c.setIDCategory(Integer.parseInt(jTextFieldID.getText()));
                                c.ss.setSubSectionName(jComboBoxSubSection.getSelectedItem().toString());
                                c.ss.setSubSectionID(new SubSection().getIDFromNameSubsection(jComboBoxSubSection.getSelectedItem().toString()));
                                SubSection ss = c.ss.getSubSection(c.ss.getSubSectionID());
                                c.ss.sec.setSectionID(ss.sec.getSectionID());
                                if (c.Edit(new Category().searchForGetCategoryID(jTextFieldSearchKey.getText()))) {
                                    resetPanel();
                                    jLabelSuccessOrFail.setText("Successfully Updated ...! :)");
                                } else {
                                    jLabelSuccessOrFail.setText("error in Updated ...! :(");
                                }
                            } else {
                                jLabelSuccessOrFail.setText("Not Found ...! :( ");
                                 jPanel3.setVisible(true);
                                 Frist_Half();
                                
                            }
                    }
                    } else {
                        JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                        jPanel9.setVisible(true);
                           second_Half();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Not valid ID Try Again :(\nHint: ID Length should more than 4 .. ;)");
                    jPanel5.setVisible(true);
                       second_Half();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                jPanel3.setVisible(true);
                   second_Half();
            }
        } else {
            jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
               second_Half();
        }
    }

