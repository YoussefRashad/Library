    private void AddSubSections() {
          jPanelWrongID.setVisible(false);
        jPanelWrongSubSectionName.setVisible(false);
        if (!jTextFieldSubSectionName.getText().equals("") && !jTextFieldSubSectionID.getText().equals("")) {
            if (jComboBoxSectionId.getItemCount() != 0) {
                Validation c = new Validation();
                if (c.checkID(jTextFieldSubSectionID.getText())) {
                    if (c.checkName(jTextFieldSubSectionName.getText())) {
                        SubSection ss = new SubSection();
                        if (!ss.Dublicate(Integer.parseInt(jTextFieldSubSectionID.getText()))) {
                            ss.setSubSectionID(Integer.parseInt(jTextFieldSubSectionID.getText()));
                            ss.setSubSectionName(jTextFieldSubSectionName.getText());
                            ss.sec.setSectionID(Integer.parseInt(jComboBoxSectionId.getSelectedItem().toString()));
                            if (ss.Add()) {
                                jLabelSubSection.setText("Added Successfully...!");
                                jComboBoxSectionId.setSelectedIndex(0);
                                this.reset();

                            } else {
                                jLabelSubSection.setText("not Added....!");
                            }
                        } else {
                            jLabelSubSection.setText("Not Allowed New Sub Section ID ");
                            jPanelWrongID.setVisible(true);
                            JOptionPane.showMessageDialog(null, "Not Allowed  Sub Section ID, Sub Section  ID is used. Please Try Again.");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "wrong Name");
                        jPanelWrongSubSectionName.setVisible(true);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "wrong ID");
                    jPanelWrongID.setVisible(true);
                }
            } else {
                jLabelSubSection.setText("No Sections found");
            }

        } else {
            jLabelSubSection.setText("missing required field...please complete all fields");
        }
    }

