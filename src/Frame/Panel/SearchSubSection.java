    private void Updatepanal() {
        visible();
        Frist_Half();
        if (!jTextFieldSectionID.getText().equals("") && !jTextFieldSubSearchKey.getText().equals("")
                && !jTextFieldSectionID.getText().equals("") && !jTextFieldSubSectionName3.getText().equals("")) {
            Validation c = new Validation();
            second_Half();
            if (c.checkID(jTextFieldSectionID.getText())) {
                if (c.checkID(jTextFieldSubSectionID1.getText())) {
                    if (c.checkName(jTextFieldSubSectionName3.getText())) {
                        SubSection s = new SubSection();
                        if (!jTextFieldSubSectionID1.getText().equals(jTextFieldSubSearchKey.getText())) {
                            if (!s.Dublicate(Integer.parseInt(jTextFieldSectionID.getText()))) {
                                s.sec.setSectionID(Integer.parseInt(jTextFieldSectionID.getText()));
                                s.setSubSectionID(Integer.parseInt(jTextFieldSubSectionID1.getText()));
                                s.setSubSectionName(jTextFieldSubSectionName3.getText());
                                if (s.Edit(Integer.parseInt(jTextFieldSubSearchKey.getText()))) {
                                    jlabesublsection.setText("updated successfuly");
                                    reset();
                                } else {
                                    jlabesublsection.setText("not updated");
                                }
                            } else {
                                jlabesublsection.setText("Not Allowed New Section ID ");
                                jPanelWrongNotAllowed.setVisible(true);
                                JOptionPane.showMessageDialog(null, "Not Allowed Section ID,Section ID is used. Please Try Again.");
                                second_Half();
                            }
                        } else {
                            s.sec.setSectionID(Integer.parseInt(jTextFieldSectionID.getText()));
                            s.setSubSectionID(Integer.parseInt(jTextFieldSubSectionID1.getText()));
                            s.setSubSectionName(jTextFieldSubSectionName3.getText());
                            if (s.Edit(Integer.parseInt(jTextFieldSubSearchKey.getText()))) {

                                jlabesublsection.setText("updated successfuly");
                                reset();
                            } else {
                                jlabesublsection.setText("not updated");
                            }
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "wrong subSection name");
                        jPanelWrongSubSectionName.setVisible(true);
                        second_Half();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "wrong subSection ID");
                    jPanelWrongSubSectionID.setVisible(true);
                    second_Half();
                }

            } else {
                JOptionPane.showMessageDialog(null, "wrong Section ID");
                jPanelWrongID.setVisible(true);
                second_Half();
            }

        } else {
            jlabesublsection.setText("missing required field...please complete all fields");
            second_Half();
        }
    }

    private void Delete() {
        visible();
        Frist_Half();
        if (!jTextFieldSectionID.getText().equals("") && !jTextFieldSubSearchKey.getText().equals("")) {
            SubSection s = new SubSection();
            if (s.Remove(Integer.parseInt(jTextFieldSubSectionID1.getText()))) {
                second_Half();
                jlabesublsection.setText("deleted successfuly");
                reset();
            } else {
                jlabesublsection.setText("not deleted");
                jPanelWrongSearchKey.setVisible(true);
                Frist_Half();
            }
        } else {
            jlabesublsection.setText("missing required field...please enter ID ...! :)");
            Frist_Half();

        }
    }

    private void Search() {
        visible();
        Frist_Half();

        if (!jTextFieldSubSearchKey.getText().equals("")) {
            Validation c = new Validation();
            if (c.checkID(jTextFieldSubSearchKey.getText())) {
                SubSection s = new SubSection();
                SubSection returned = s.getSubSection(Integer.parseInt(jTextFieldSubSearchKey.getText()));
                if (returned != null) {
                    second_Half();
                    jTextFieldSectionID.setText("" + returned.sec.getSectionID());
                    jTextFieldSubSectionID1.setText("" + returned.getSubSectionID());
                    jTextFieldSubSectionName3.setText(returned.getSubSectionName());
                    jlabesublsection.setText("Found ...! :)");
                } else {
                    jlabesublsection.setText("Not Found ...! :(");
                    Frist_Half();
                }

            } else {
                JOptionPane.showMessageDialog(null, "wrong ID");
                jPanelWrongSearchKey.setVisible(true);
                Frist_Half();

            }
        } else {
            jlabesublsection.setText("missing required field...please enter ID ...! :)");
            Frist_Half();
        }
    }

