    private void AddEmp() {
        visable();

        if (!jTextFieldID.getText().equals("") && !jTextFieldName.getText().equals("") && !jTextFieldUserName.getText().equals("") && !jTextFieldEmail.getText().equals("") && !jPasswordFieldPassword.getText().equals("") && !jPasswordFieldRePassword.getText().equals("") && !jTextFieldIDNational.getText().equals("") && !jTextFieldPhoneON.getText().equals("") && !jTextFieldAge.getText().equals("") && !jTextFieldAdress.getText().equals("") && !jTextFieldOfficeHour.getText().equals("") && !jTextFieldqualification.getText().equals("") && !jTextFieldJobTitle.getText().equals("") && !jTextFieldSalary.getText().equals("")) {
            // AllCheck
            Validation v = new Validation();
            if (v.checkID(jTextFieldID.getText())) {
                if (v.checkName(jTextFieldName.getText())) {
                    if (v.checkUserName(jTextFieldUserName.getText())) {
                        if (v.checkEmail(jTextFieldEmail.getText())) {
                            if (v.checkPass(jPasswordFieldPassword.getText())) {
                                if (v.checkPass(jPasswordFieldRePassword.getText())) {
                                    if (v.checkIDNational(jTextFieldIDNational.getText())) {
                                        if (v.checkPhoneNo(jTextFieldPhoneON.getText())) {
                                            if (v.checkAge(jTextFieldAge.getText())) {
                                                if (v.checkAddress(jTextFieldAdress.getText())) {
                                                    if (v.checkName(jTextFieldqualification.getText())) {
                                                        if (v.checkSalary(jTextFieldSalary.getText())) {
                                                            /// if (v.checkID(jTextFieldOfficeHour.getText())) {

                                                            Employee e = new Employee();
                                                            if (!e.Dublicate(Integer.parseInt(jTextFieldID.getText()))) {
                                                                if (jPasswordFieldPassword.getText().equals(jPasswordFieldRePassword.getText())) {
                                                                    if ((Integer.parseInt(jTextFieldOfficeHour.getText()) > 0) && (Integer.parseInt(jTextFieldOfficeHour.getText()) < 24)) {
                                                                        e.setAddress(jTextFieldAdress.getText());
                                                                        e.setAge(Integer.parseInt(jTextFieldAge.getText()));
                                                                        e.setDOB(jComboBoxyearBirth1.getSelectedItem().toString() + "#" + jComboBoxmonthBirth1.getSelectedItem().toString() + "#"
                                                                                + jComboBoxdayBirth1.getSelectedItem().toString() + "#");
                                                                        e.setEmail(jTextFieldEmail.getText());
                                                                        if ((jComboBoxGender.getSelectedItem().toString()).equals("Male")) {
                                                                            e.setGender('M');
                                                                        } else {
                                                                            e.setGender('F');
                                                                        }
                                                                        e.setID(Integer.parseInt(jTextFieldID.getText()));
                                                                        e.setIDNational((jTextFieldIDNational.getText()));
                                                                        e.setName(jTextFieldName.getText());
                                                                        e.setPhoneNo(jTextFieldPhoneON.getText());
                                                                        e.setUserName(jTextFieldUserName.getText());
                                                                        e.setPass(jPasswordFieldPassword.getText());
                                                                        e.setOfficeHour(Integer.parseInt(jTextFieldOfficeHour.getText()));

                                                                        e.setJobTitle(jTextFieldJobTitle.getText());
                                                                        e.setSalary(Double.parseDouble(jTextFieldSalary.getText()));
                                                                        e.setQualification(jTextFieldqualification.getText());
                                                                        if (e.Add()) {
                                                                            jLabelSuccessOrFail.setText("Added Successfully ...! :) ");
                                                                            resetPanel();
                                                                        } else {
                                                                            jLabelSuccessOrFail.setText("Failed To Insert...!");
                                                                        }
                                                                    } else {
                                                                        JOptionPane.showMessageDialog(null, "Not valid Office Hour Try Again :(\nHint: Ofice Hour shouid less than 24 Hour per Day.. ;)");
                                                                        jPanelWrongOfficeHours.setVisible(true);
                                                                    }
                                                                } else {
                                                                    jLabelSuccessOrFail.setText("Pass & RePass Not Similar ...!");
                                                                    JOptionPane.showMessageDialog(null, "Not valid password & Repassword not similar please Try Again :( ");
                                                                    jPanelWrongPassword.setVisible(true);
                                                                    jPanelWrongPassword.setVisible(true);

                                                                }
                                                            } else {
                                                                jLabelSuccessOrFail.setText("Not Allowed New Employee ID ");
                                                                jPanelWrongID.setVisible(true);
                                                                JOptionPane.showMessageDialog(null, "Not Allowed Employee ID,Employee ID is used. Please Try Again.");
                                                            }
                                                        } else {
                                                            JOptionPane.showMessageDialog(null, "Not valid Salary Try Again :(\nHint: Salary should more than 1000 .. ;)");
                                                            jPanelWrongSalary.setVisible(true);
                                                        }
                                                    } else {
                                                        JOptionPane.showMessageDialog(null, "Not valid Input for Qualification Try Again :(");
                                                        jPanelWrongAddress.setVisible(true);
                                                    }
                                                } else {
                                                    JOptionPane.showMessageDialog(null, "Not valid Input for Address Try Again :(");
                                                    jPanelWrongAddress.setVisible(true);
                                                }
                                            } else {
                                                JOptionPane.showMessageDialog(null, "Not valid Age Try Again :(\nHint: Age should more than 18 .. ;)");
                                                jPanelWrongAge.setVisible(true);
                                            }
                                        } else {
                                            JOptionPane.showMessageDialog(null, "Not valid Phone No Try Again :(\nHint: Phone No should  equal 11 numbers if it mobile\n or more than 8 with Code of Governorate if it Tel  .. ;)");
                                            jPanelWrongPhoneNo.setVisible(true);
                                        }
                                    } else {
                                        JOptionPane.showMessageDialog(null, "Not valid ID National Try Again :(\nHint: ID National Length should equal to 14 Number .. ;)");
                                        jPanelWrongIDNational.setVisible(true);
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "\"Not valid RePassword length ,Should it Length\\nis more than 6 OR Password has (*,/,=,+,...)\\n ");
                                    jPanelWrongRePassword.setVisible(true);
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Not valid Password length ,Should it Length\nis more than 6 OR Password has (*,/,=,+,...)\n ");
                                jPanelWrongPassword.setVisible(true);
                            }

                        } else {
                            JOptionPane.showMessageDialog(null, "\"Not valid Input for Email Try Again :(\"");
                            jPanelWrongEmail.setVisible(true);

                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "\"Not valid User Name Try Again :(\"");
                        jPanelWrongUserName.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                    jPanelWrongName.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Not valid ID Try Again :(\nHint: ID Length should more than 4 .. ;)");
                jPanelWrongID.setVisible(true);
            }
        } else {
            jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :)");
        }
    }

