    private void Search() {
        resetPanel();
        jPanelWrongName.setVisible(false);
         if(!jTextFieldSearchKey.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearchKey.getText())){
                Book b = new Book();
                b=b.getBook(Integer.parseInt(jTextFieldSearchKey.getText()));
                if(b!=null){
                    jLabelSuccessOrFail.setText("Founded ...! :) ");
                    setPanel(b);
                }
                else
                jLabelSuccessOrFail.setText("Not Found ...! :( ");
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid Name Of Book Try Again :(");
               jPanelWrongName.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing Required Name Book ... !  Please, complete it before submit ...! :(");
    }
