  public void AddRowsToTable() {
      resetTable();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] rowData = new Object[3];
        ArrayList<SubSection> subsection= new SubSection().ListSpecificSubSection(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
        for (SubSection a : subsection) {
            rowData[0] = a.getSubSectionID();
            rowData[1] = a.getSubSectionName();
            rowData[2] = a.sec.getSectionID();
            model.addRow(rowData);
        }
    }
    private void resetTable() {
         try{
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            while(jTable1.getRowCount()>0){
                for(int i=0;i<jTable1.getRowCount();i++)
                    model.removeRow(i);
            }
            model.removeRow(0);
        }
        catch(Exception e)
        {
            ;
        }
    }
