    private void jPanelSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelSearchMouseClicked
        // TODO add your handling code here:
        if(jComboBoxCategories.getItemCount()>0){
            Category c = new Category();
            c = c.getCategory(jComboBoxCategories.getSelectedItem().toString());
            if(c!=null){
                jTextFieldSection.setText(c.ss.sec.getSectionID()+"");
                jTextFieldSubSection.setText(c.ss.getSubSectionName());
                setTable(c);
            }
            else
                jLabelSuccessOrFail.setText("Not found Category in System :(");
        }else
            jLabelSuccessOrFail.setText("Not found any Categories in System :(");
    }//GEN-LAST:event_jPanelSearchMouseClicked

